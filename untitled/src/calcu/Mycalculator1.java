package calcu;

public class Mycalculator1{

    public static int sub(int n1,int n2) {
        return (n1 - n2);
    }
    public static int add(int n1,int n2) {
        return (n1 + n2);
    }
    public static int multi(int n1,int n2) {
        return n1 * n2;
    }
    public static int divi(int n1,int n2) {
        return n1 / n2;
    }
}
